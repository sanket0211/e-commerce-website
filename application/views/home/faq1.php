        <div class="togglec">When a person wishes to borrow an item, he must send a request to the giver of the product.
When the giver accepts the request for the item, two codes are generated. One is shown to the Giver, called the Giver's code and the other to the Borrower, called the Borrower's code.
Now while exchanging the item, the Giver has to enter the Borrower's code to start the renting period, without which the renting cannot start. At the time of returning the item, the Borrower can ask the Giver for the Giver's code to stop the renting period.
Thus, the renting contract comes to an end.
